export function gettingStartedTemplate(author: string): string {
  return `# 캐논은 이야기의 약속이다

> 쓰여진 세계관은 사라지지 않는다. 참조될수록 커진다.

opencanon은 함께 만드는 세계관 프로토콜입니다.
쓴 이야기가 세계관과 맞는지 확인하고, 저장소에 기록으로 남깁니다.

---

## 시작 전에

당신이 만들 것은 단순한 이야기가 아닙니다.  
**캐논(Canon)** — 이 세계에서 실제로 일어난 것들의 기록입니다.

누군가 다음 에피소드를 쓸 때, 당신의 캐논이 기준이 됩니다.  
그래서 캐논은 정확해야 하고, 일관돼야 합니다.

---

## 세계관 만들기

### 1단계: 세계의 기둥을 세운다

캐릭터와 장소를 먼저 정의하세요.  
이야기보다 세계가 먼저입니다.

\`\`\`bash
# 주인공 정의
canon new character <이름>

# 주요 장소 정의
canon new location <장소명>
\`\`\`

**좋은 캐릭터 정의란:**
- 이름과 역할만으로는 부족합니다
- 이 세계에서 무엇을 원하는지, 무엇을 두려워하는지
- 다른 캐릭터와의 관계가 세계를 만듭니다

**좋은 장소 정의란:**
- 물리적 설명보다 *분위기*가 중요합니다
- 이 장소에서 어떤 일이 일어날 수 있는지
- 처음 등장하는 에피소드를 \`first_appearance\`에 남기세요

---

### 2단계: 첫 번째 에피소드를 쓴다

\`\`\`bash
canon new story <에피소드-슬러그>
\`\`\`

에피소드 ID 규칙: \`ep01-awakening\`, \`ep02-echo\` 형식 권장  
소문자, 숫자, 하이픈만 가능합니다.

**metadata.json을 채우는 법:**

| 필드 | 설명 |
|------|------|
| \`title\` | 한국어 + 영어 제목 |
| \`timeline\` | 이야기 속 날짜 (YYYY-MM-DD) |
| \`synopsis\` | 한 문장 요약. 독자가 읽기 전 유일하게 보는 것 |
| \`characters\` | 등장 캐릭터 목록. 반드시 정의된 것만 |
| \`locations\` | 등장 장소 목록. 반드시 정의된 것만 |
| \`canon_status\` | \`canonical\` 또는 \`non-canonical\` |

**synopsis 작성 팁:**
> "누가, 어디서, 무엇을 처음으로 했다"  
> 결말을 담되, 감정은 뺍니다. 감정은 본문에서 전달됩니다.

---

### 3단계: 본문을 쓴다

\`stories/<슬러그>/chapter-01.md\` 파일을 만들고 씁니다.  
파일명 형식은 자유롭지만, 챕터 번호를 포함하길 권장합니다.

**캐논 집필 원칙:**
1. **일관성** — 정의한 캐릭터/장소에서 벗어나지 않습니다
2. **비가역성** — 캐논으로 기록된 사건은 되돌릴 수 없습니다
3. **참조 가능성** — 다음 에피소드가 이 사건을 언급할 수 있어야 합니다

---

### 4단계: 캐논을 잠근다

\`\`\`bash
# 커밋 먼저
git add -A && git commit -m "[story] ep01: ..."

# 캐논 잠금
canon lock --update-refs

# 검증
canon check
\`\`\`

모든 검사가 통과하면 이야기가 캐논에 기록됩니다.

---

## 수첩 활용

복선, 메모, 자유로운 생각은 수첩에 남기세요.  
캐논에 기록되지 않은 아이디어들입니다.

\`NOTEBOOK.md\` 파일을 만들어 자유롭게 씁니다.  
이곳의 기록은 다음 집필에 흡수된 뒤 사라져도 됩니다.

---

## 준수 검사 항목

| 검사 | 의미 |
|------|------|
| \`metadata_schema_valid\` | 필수 필드가 모두 있는가 |
| \`characters_valid\` | 등장 캐릭터가 정의되어 있는가 |
| \`locations_valid\` | 등장 장소가 정의되어 있는가 |
| \`timeline_consistent\` | 날짜 형식이 올바른가 (YYYY-MM-DD) |
| \`continuity_valid\` | 이전/다음 에피소드 참조가 유효한가 |
| \`canon_version_match\` | 현재 캐논 버전과 일치하는가 |
| \`contributor_valid\` | 기여자 이름이 유효한 형식인가 |

---

## 협업할 때

다른 사람이 이 세계에서 이야기를 쓰려면:
1. 이 저장소를 fork 또는 clone합니다
2. \`canon check\`로 현재 캐논을 확인합니다
3. 새 에피소드를 쓰고 PR로 제출합니다

**기여자 이름**: \`metadata.json\`의 \`contributor\` 필드에 본인 이름/아이디를 씁니다.  
GitHub 없이도 쓸 수 있습니다. 로컬 git만 있으면 됩니다.

---

*이 파일은 \`canon init\`이 생성했습니다. 자유롭게 수정하세요.*  
*작성자: ${author || "unknown"}*
`
}
